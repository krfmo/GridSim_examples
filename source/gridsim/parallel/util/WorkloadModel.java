package gridsim.parallel.util;

import java.util.List;

/**
 * This interface defines what a workload model should provide. A workload
 * model generates a list of jobs that can be dispatched to a resource by
 * {@link Workload}.
 *
 * @author   Marcos Dias de Assuncao
 * @since 5.0
 * 
 * @see Workload
 * @see WorkloadFileReader
 */
public interface WorkloadModel {

	/**
	 * Returns a list with the jobs generated by the workload.
	 * @return a list with the jobs generated by the workload.
	 */
	List<WorkloadJob> generateWorkload();	
	
}

